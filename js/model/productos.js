class Producto {
    id;
    descripcion;
    imagen;
    precio;
    procesador;
    marca;
    categoria;
    alt;
    
    constructor(id,descripcion,imagen,precio,procesador,marca,categoria,alt) {
        this.id = id;
        this.descripcion = descripcion;
        this.imagen = imagen;
        this.precio = precio;
        this.procesador = procesador;
        this.marca = marca;
        this.categoria = categoria;
        this.alt = alt;
    }
}

